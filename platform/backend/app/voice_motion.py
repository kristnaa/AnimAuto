def _transcription_to_dict(transcription: Any) -> dict[str, Any]:
    if hasattr(transcription, "model_dump"):
        return transcription.model_dump()
    if isinstance(transcription, dict):
        return transcription
    return {"text": str(transcription), "segments": []}


def transcribe_audio(openai_client: Any, audio_path: Path) -> dict[str, Any]:
    with audio_path.open("rb") as handle:
        raw = openai_client.audio.transcriptions.create(
            model="whisper-1",
            file=handle,
            response_format="verbose_json",
            timestamp_granularities=["segment"],
        )
    data = _transcription_to_dict(raw)
    segments: list[dict[str, Any]] = []
    for seg in data.get("segments") or []:
        if isinstance(seg, dict):
            start = float(seg.get("start", 0))
            end = float(seg.get("end", start))
            text = str(seg.get("text", "")).strip()
        else:
            start = float(getattr(seg, "start", 0))
            end = float(getattr(seg, "end", start))
            text = str(getattr(seg, "text", "")).strip()
        if text:
            segments.append({"start": round(start, 2), "end": round(end, 2), "text": text})
    duration = float(data.get("duration") or (segments[-1]["end"] if segments else 0))
    transcript = str(data.get("text") or " ".join(s["text"] for s in segments)).strip()
    return {
        "transcript": transcript,
        "duration_sec": round(duration, 2),
        "segments": normalize_segments(segments),
    }


def normalize_segments(segments: list[dict[str, Any]], max_segments: int = MAX_PLAN_SEGMENTS) -> list[dict[str, Any]]:
    if len(segments) <= max_segments:
        return segments
    merged: list[dict[str, Any]] = []
    bucket: list[dict[str, Any]] = []
    target = len(segments) / max_segments
    count = 0.0
    for seg in segments:
        bucket.append(seg)
        count += 1
        if count >= target and len(merged) < max_segments - 1:
            merged.append(_merge_segment_bucket(bucket))
            bucket = []
            count = 0.0
    if bucket:
        if merged:
            merged[-1] = _merge_segment_bucket([merged[-1], *bucket])
        else:
            merged.append(_merge_segment_bucket(bucket))
    return merged[:max_segments]


def _merge_segment_bucket(bucket: list[dict[str, Any]]) -> dict[str, Any]:
    return {
        "start": bucket[0]["start"],
        "end": bucket[-1]["end"],
        "text": " ".join(s["text"] for s in bucket).strip(),
    }


def build_motion_plan(openai_client: Any, model: str, segments: list[dict[str, Any]], transcript: str) -> list[dict[str, Any]]:
    payload = json.dumps({"transcript": transcript, "segments": segments}, indent=2)
    system = (
        "You plan motion-graphics visuals for a Manim voice-over video. "
        "Return JSON: {\"motion_plan\": [{\"start\", \"end\", \"narration\", \"visuals\": [str], "
        "\"mobjects\": [str]}]}. One plan item per segment. "
        "visuals = short animation instructions; mobjects = Manim classes to use. "
        "No cards, icons, or external images."
    )
    response = openai_client.chat.completions.create(
        model=model,
        messages=[
            {"role": "system", "content": system},
            {"role": "user", "content": f"Plan visuals for these timed segments:\n{payload}"},
        ],
        response_format={"type": "json_object"},
        temperature=0.4,
    )
    raw = response.choices[0].message.content or "{}"
    data = json.loads(raw)
    plan = data.get("motion_plan") or data.get("segments") or []
    if not isinstance(plan, list):
        return []
    out: list[dict[str, Any]] = []
    for i, item in enumerate(plan):
        if not isinstance(item, dict):
            continue
        seg = segments[i] if i < len(segments) else {}
        out.append(
            {
                "start": float(item.get("start", seg.get("start", 0))),
                "end": float(item.get("end", seg.get("end", 0))),
                "narration": str(item.get("narration") or seg.get("text") or ""),
                "visuals": list(item.get("visuals") or []),
                "mobjects": list(item.get("mobjects") or []),
            }
        )
    if not out and segments:
        for seg in segments:
            out.append(
                {
                    "start": seg["start"],
                    "end": seg["end"],
                    "narration": seg["text"],
                    "visuals": ["FadeIn white title text for narration"],
                    "mobjects": ["Text"],
                }
            )
    return out


def generate_motion_scene(
    openai_client: Any,
    model: str,
    motion_plan: list[dict[str, Any]],
    duration_sec: float,
    *,
    existing_code: str | None = None,
    edit_message: str | None = None,
) -> str:
    skill = load_voice_motion_skill()
    plan_json = json.dumps({"duration_sec": duration_sec, "motion_plan": motion_plan}, indent=2)
    user_parts = [f"Motion plan (seconds):\n{plan_json}"]
    if existing_code and edit_message:
        user_parts.append(f"Current scene code:\n```python\n{existing_code}\n```")
        user_parts.append(f"Edit request: {edit_message}")
    else:
        user_parts.append("Generate the complete VoiceMotionScene Python file.")
    response = openai_client.chat.completions.create(
        model=model,
        messages=[
            {"role": "system", "content": skill + "\n\nReturn JSON: {\"code\": \"...full python...\"}"},
            {"role": "user", "content": "\n\n".join(user_parts)},
        ],
        response_format={"type": "json_object"},
        temperature=0.35,
    )
    raw = response.choices[0].message.content or "{}"
    data = json.loads(raw)
    code = str(data.get("code") or "")
    if not code.strip():
        raise ValueError("LLM returned empty scene code")
    return sanitize_motion_scene(code)


def sanitize_motion_scene(code: str) -> str:
    code = code.strip()
    if code.startswith("```"):
        code = re.sub(r"^```(?:python)?\n?", "", code)
        code = re.sub(r"\n?```$", "", code)
    issues = validate_motion_scene(code)
    if issues:
        raise ValueError("; ".join(issues[:3]))
    if f"class {SCENE_CLASS}" not in code:
        code = code.replace("class GeneratedScene", f"class {SCENE_CLASS}")
    if "from manim import" not in code:
        code = "from manim import *\n\n" + code
    return code + ("\n" if not code.endswith("\n") else "")


def validate_motion_scene(code: str) -> list[str]:
    issues: list[str] = []
    for forbidden in FORBIDDEN_IMPORTS:
        if forbidden in code:
            issues.append(f"Forbidden import/module: {forbidden}")
    for name in FORBIDDEN_NAMES:
        if name in code:
            issues.append(f"Forbidden symbol: {name}")
    if "class " not in code or "construct" not in code:
        issues.append("Scene must define a class with construct()")
    try:
        tree = ast.parse(code)
    except SyntaxError as exc:
        issues.append(f"Syntax error: {exc}")
        return issues
    for node in ast.walk(tree):
        if isinstance(node, ast.ImportFrom) and node.module:
            for forbidden in FORBIDDEN_IMPORTS:
                if forbidden in (node.module or ""):
                    issues.append(f"Forbidden import: {node.module}")
    return issues


