def run_voice_pipeline(
    openai_client: Any,
    model: str,
    audio_path: Path,
    *,
    existing_code: str | None = None,
    edit_message: str | None = None,
    skip_transcribe: bool = False,
    existing_voice: dict[str, Any] | None = None,
) -> dict[str, Any]:
    if skip_transcribe and existing_voice:
        transcript_data = {
            "transcript": existing_voice.get("transcript", ""),
            "duration_sec": existing_voice.get("duration_sec", 0),
            "segments": existing_voice.get("segments") or [],
        }
    else:
        transcript_data = transcribe_audio(openai_client, audio_path)
    motion_plan = build_motion_plan(
        openai_client,
        model,
        transcript_data["segments"],
        transcript_data["transcript"],
    )
    code = generate_motion_scene(
        openai_client,
        model,
        motion_plan,
        float(transcript_data["duration_sec"] or 0),
        existing_code=existing_code,
        edit_message=edit_message,
    )
    return {
        **transcript_data,
        "motion_plan": motion_plan,
        "code": code,
    }


def edit_motion_scene(
    openai_client: Any,
    model: str,
    project: dict[str, Any],
    edit_message: str,
    existing_code: str,
) -> dict[str, Any]:
    voice = project.get("voice_motion") or {}
    motion_plan = voice.get("motion_plan") or []
    duration = float(voice.get("duration_sec") or 0)
    code = generate_motion_scene(
        openai_client,
        model,
        motion_plan,
        duration,
        existing_code=existing_code,
        edit_message=edit_message,
    )
    return {"code": code, "message": "Updated motion scene from your edit."}
